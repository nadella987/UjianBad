    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
            try {
            pst = db.con.prepareStatement("insert into data_siswa (nama, jenis_kelamin, alamat, kelas) VALUES (?,?,?,?)");
            pst.setString(1, txtnama.getText());
            String jenisKelamin = rbnlaki.isSelected() ? "Laki-laki" : "Perempuan";
            pst.setString(2, jenisKelamin);
            pst.setString(3, txtalamat.getText());
            pst.setString(4, txtkelas.getText());

            pst.executeUpdate(); 
            loadData();
            JOptionPane.showMessageDialog(null, "Data Siswa tersimpan");
            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
           try {
        int row = tblDataSiswa.getSelectedRow();
        // Mengambil nilai id dari kolom pertama (indeks 0)
        String id = tblDataSiswa.getValueAt(row, 0).toString(); 

        pst = db.con.prepareStatement("UPDATE data_siswa SET nama=?, jenis_kelamin=?, alamat=?, kelas=? WHERE id=?");
        pst.setString(1, txtnama.getText());
        String jenisKelamin = rbnlaki.isSelected() ? "Laki-laki" : "Perempuan";
        pst.setString(2, jenisKelamin);
        pst.setString(3, txtalamat.getText());
        pst.setString(4, txtkelas.getText());
        pst.setString(5, id); // Menggunakan nilai id dari baris yang dipilih
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Siswa berhasil terupdate");
        pst.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblDataSiswa.getSelectedRow();
        String idHapus = tblDataSiswa.getValueAt(row, 0).toString();
        String sql = "delete from data_siswa where id='"+idHapus+"'";
        
        try {
            st = db.con.createStatement();
            st.execute(sql);
            loadData();
            JOptionPane.showMessageDialog(null, "Data Siswa berhasil dihapus");
            st.close();   
        } catch (Exception e){
        JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblDataSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataSiswaMouseClicked
        // TODO add your handling code here:
        int row = tblDataSiswa.getSelectedRow();
        txtnama.setText(tblDataSiswa.getValueAt(row, 1).toString());
        
        String jeniskelamin = tblDataSiswa.getValueAt(row, 2).toString();

        if ("Laki-laki".equals(jeniskelamin)) {
            rbnlaki.setSelected(true);
            rbnperempuan.setSelected(false);
        } else if ("Perempuan".equals(jeniskelamin)) {
            rbnlaki.setSelected(false);
            rbnperempuan.setSelected(true);
        }
        txtalamat.setText(tblDataSiswa.getValueAt(row, 3).toString());
        txtkelas.setText(tblDataSiswa.getValueAt(row, 4).toString());
       
        
    }//GEN-LAST:event_tblDataSiswaMouseClicked

