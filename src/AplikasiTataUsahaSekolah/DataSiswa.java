public void cari() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("id");
    model.addColumn("nama");
    model.addColumn("jenis_kelamin");
    model.addColumn("alamat");
    model.addColumn("kelas");

    try {
        String cari = txtCari.getText(); // Kolom input pencarian
        
        String sql = "SELECT * FROM data_siswa WHERE nama LIKE '%" + cari + "%' OR kelas LIKE '%" + cari + "%'";
        
        st = db.con.createStatement();
        rs = st.executeQuery(sql);
        
        while (rs.next()) {
            model.addRow(new String[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5)
            });
        }
        
        tblDataSiswa.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }


    }
